char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	*r;

	i = 0;
	r = (char *)s;
	while (r[i])
	{
		if (c == (int)(r[i]))
			return (&r[i]);
		i++;
	}
	if (c == '\0')
		return ("\0");
	return (0);
}
